    class Solution {
    public int[] sortedSquares(int[] nums) {
        int left = 0;
        int right = nums.length - 1;
        int k = right;
        int[] result = new int[nums.length];
        // [-4,-1,0,3,10]
        while(left <= right){
            int leftNums = nums[left] * nums[left];
            int rightNums = nums[right] * nums[right];
            if(leftNums > rightNums){
                result[k--] = leftNums;
                left++;
            }else{
                result[k--] = rightNums;
                right--;
            }
        }

        return result;

    }
}
